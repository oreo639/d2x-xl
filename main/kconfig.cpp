	return nType;

for (i = 0, n = (int) (item - m_items); i < m_nItems; i++) {
	if ((i != n) && (m_items [i].nType == nType) && (m_items [i].value == code)) {
		m_items [i].value = 255;
		if (ogl.m_states.nDrawBuffer == GL_FRONT)
			DrawItem (m_items + i, 0);
		}
	}
